"""
Operadores aritiméticos
+, soma
-, subtração
*, multiplicação
/, divisão
//, divisão inteira (arrendondada)
%, resto da divisão
(), para fazer uma conta primeiro - ordem de precedencia
"""

print('Soma +', 10 + 10)
print('Subtração -', 10 - 10)
print('Multiplicação *', 10 * 10)
print('Divisão /', 10 / 10)
print('Divisão inteira //', 10.5 / 3)
print('Exponenciação **', 10 ** 2)
print('Resto da divisão %', 10 % 3)